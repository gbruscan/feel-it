#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*dest;

	dest = (char *)s;
	i = 0;
	while (n > 0)
	{
		dest[i] = '\0';
		i++;
		n--;
	}
}
